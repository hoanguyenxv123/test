    public void LoadCbb() {
        cbbMa.setSelectedIndex(-1);
        ResultSet rs = thucThi.HienThicbb();
        try {
            while (rs.next()) {
                String ma = rs.getString(1);
                cbbMa.addItem(ma);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Sach.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void LoadTT() {
        model.setRowCount(0);
        ResultSet rs = thucThi.hienThiSach();
        try {
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString(1), rs.getString(2), rs.getString(3)});
            }
        } catch (SQLException ex) {
            Logger.getLogger(Sach.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

