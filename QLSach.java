    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int vitri = jTable1.getSelectedRow();
        if (vitri > 0) {
            vitri--;
            txtMa.setText(model.getValueAt(vitri, 0).toString());
            txtTen.setText(model.getValueAt(vitri, 1).toString());
            cbbMa.setSelectedItem(model.getValueAt(vitri, 2).toString());
            jLabel1.setText((vitri + 1) + "/" + jTable1.getRowCount());
            jTable1.getSelectionModel().setSelectionInterval(vitri, vitri);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int vitri = jTable1.getSelectedRow();
        if (vitri < jTable1.getRowCount() - 1) {
            vitri++;
            txtMa.setText(model.getValueAt(vitri, 0).toString());
            txtTen.setText(model.getValueAt(vitri, 1).toString());
            cbbMa.setSelectedItem(model.getValueAt(vitri, 2).toString());
            jLabel1.setText((vitri + 1) + "/" + jTable1.getRowCount());
            jTable1.getSelectionModel().setSelectionInterval(vitri, vitri);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

